from random import randint
import string

def write_f(path, num):
	with open(path, 'w', encoding='utf-8') as f:
		f.write(num)

def read_f(path):
	with open(path, 'r', encoding='utf-8') as f:
           content = f.read()
           f.close()
           return content

x = str([randint(0, 9) for p in range(0, 2500)])
